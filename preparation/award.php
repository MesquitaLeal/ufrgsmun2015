                <h5>Award Policy</h5>
                <br/>
                <p>UFRGSMUN rewards representatives when they prove to be truly involved with the “Model United Nations spirit”, aware of their delegation’s role and acting in a diplomatic way. For individual awards, coherence in foreign policy, diplomatic behaviour, speaking ability, negotiation skills, knowledge of issues and rules of procedure and the votes of the delegates are taken into account. For delegations awards, the individual performances of the delegates will be analyzed from a collective perspective, with regard to the delegation’s cohesion and coherence.</p>
                <p>We strongly stress that UFRGSMUN is an educational simulation aiming at encouraging all delegates to prepare to represent either members of the International Law Commission, ambassadors, or representatives of the civil society in one of the other bodies of the United Nations.</p>
                <p>At the end of UFRGSMUN, we hope that each student will have an enriching educational experience, learning both from his own studies and from his interactions with his fellow delegates and chair persons.</p>
                <p>The spirit of the prize is not to enhance competition among delegates, but to reward those that have indeed made their best, setting them as examples as encouraging further improved behaviour.</p>
