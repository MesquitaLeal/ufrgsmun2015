                <h5>Position Paper</h5>
                <p>
                    The most common definition for a position paper is a statement of your assigned
                    country’s (or intergovernmental organization or non-governmental organization’s)
                    views on the matter to be discussed. While this definition may not be incorrect,
                    it is rather vague and incomplete. Thus it is necessary to clarify exactly what
                    this document (of usually one page per topic in length) entails.
                </p>
                <p>
                    First of all, it is very important to offer a brief history of the matter regarding
                    your country, explaining how it has been affected by the issue. You must then state
                    your country’s policies, as well as its justifications for them, followed by the
                    actions that have been taken so far, including the signature and ratification of
                    treaties and conventions and involvement in UN (or other organization’s) initiatives.
                    A good position paper preferably contains statistics and other more specific data to
                    better illustrate the actions your country has been undertaking. You should conclude
                    your position paper with ideas and concrete proposals, making sure that they address
                    the points raised in the section “Questions to Ponder” (or its equivalent) of the study
                    guides.
                </p>
                <p>
                    You must also pay attention to the presentation of your position paper. Instead of
                    using the country’s flag in the header (a mistake even more experienced delegates
                    often incur in), opt for your country’s official seal whenever possible. This
                    obviously excludes committees in which the delegate acts in his or her personal
                    capacity, such as the International Court of Justice. In these cases, no national
                    symbols are to be used.
                </p>
                <p>
                    Try to stick to the recommended length. If your position paper is too short, it will
                    most likely fail to tackle all the points it should. If it is excessively long, you
                    will probably either repeat yourself or offer more information than necessary—which
                    is rarely beneficial when it comes to negotiation—and most delegates will not read it
                    to its entirety.
                </p>
                <p>
                    Finally, the language used in a position paper is also of utmost importance. As with any
                    official document, it must be simple, clear, objective and concise, and always written
                    in the third person. Nevertheless, it is vital to remember that, as official documents,
                    position papers provide the public stance adopted by the country. They must never be
                    confused with an exposition of national interests regarding the topic. Therefore, you
                    can and should make use of vagueness and ambiguity whenever your country’s situation
                    regarding the issue calls for it. This is also an extremely valuable tool for the
                    simulation itself, as your real intentions become less evident and you become freer to
                    negotiate and make compromises when necessary, thus increasing your chances of achieving
                    your country’s true objectives.
                </p>
                <p>
                    And, of course, always respect the deadlines! Handing in a position paper late will reflect
                    badly all the hard work you have done. Position papers must be submitted to the Directors
                    of your committee following the guidelines you will receive per email prior to the UFRGSMUN
                    conference. Moreover, although not mandatory, delegates are advised to hand over a copy of
                    their position papers to their committee Directors in the first session of the committee.
                </p>
