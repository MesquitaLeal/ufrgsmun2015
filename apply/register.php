<div class="row beige section-p content">
    <div class="col-lg-6">
        <h2>Step 1</h2>
        <h3 class="step-title">Putting together a delegation and<br/>selecting a head delegate</h3>
        <p>
            The first thing to do when putting together a delegation would be to find other people that wish to take part in the other committees. Next, you should select one or more countries from the Country/Committee Matrix that have representatives in all the committees that you and your friends want to participate in. Delegations may range from 1 (one) up to 20 (twenty) delegates. Then, you should select a head delegate among the members from your delegation. That person will be responsible for filling out registration forms, making payments, and staying in direct contact with UFRGSMUN staff.
            <br/>If you wish to represent a specific country, then you should invite enough people to complete the delegation of this country, settling amongst yourselves who will be in which committee. But remember that it is not certain that your first choice of country will be designated to you. The number of delegates and the countries that you wish to represent must be informed in Form A, which will be online from August 15th on.
        </p>
        <p class="step-note">NOTE: if you do not take part in a delegation and wish to be fitted in an existing one, specific guidance will be provided to you in Form A.</p>
    </div>
