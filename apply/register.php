                <p>
                    <strong>Step 2: Filling in <a href="?page=apply&section=formA">Form A</a></strong>
                </p>
                <p>
                    Once your delegation has decided which countries it would like to represent, the head
                    delegate must proceed to filling in Form A. During such procedure, the head delegate will
                    have to inform 12 (twelve) countries or combinations of countries that would please the
                    delegation the most. This is to facilitate the staff’s work in allocating preferences.
                    Throughout this process, it is important to remember that the number of delegates must
                    be equivalent to the number of spots demanded upon registration.
                </p>
                <p>
                    For example: If your delegation is composed of 16 delegates, you should choose twelve
                    combinations of countries whose sum is 16. An example of possible combination is: China
                    (6) + Pakistan (4) + Indonesia (3) + Iran (3) = 16 delegates. Or: Australia (5) +
                    Brazil (4) + Canada (3) + Cambodia (3) + Belarus (1) = 16 delegates.  If your
                    delegation is composed of only  6 delegates, you can ask for a single country,
                    like Nigeria (6), or for a combination as: Kazakhstan (3) + Jordan (2) + Liberia (1).
                </p>
                <p>
                    Once your delegation—represented by the Head Delegate—has submitted this form, he or
                    she will receive an e-mail from the staff indicating exact payment procedures.
                </p>
            </section>
