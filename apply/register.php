                    <strong>Step 3: <a href="?page=apply&section=payment">Payment</a></strong><br/>
                    A fee per person of R$ 150.00 in full amount or two installments of R$ 75,00
                    applies in order to participate in the conference. Head delegates will have
                    the chance to choose in the registration form whether their delegations will
                    pay their corresponding delegate fees in two installments or in full amount.
                    The payment method chosen necessarily applies to all members of your delegation.
                    If one or more of the delegates of a delegation were selected and approved in our
                    “Edital de Benefícios” the head delegate must send an email to the secretariat notifying this.

                </p>
