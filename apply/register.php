        <h2>Step 4</h2>
        <h3 class="step-title">Filling in Form B</h3>
        <p>Between the October 3rd and 9th, Head Delegates will receive another e-mail indicating the country assigned to the delegation. Once your country has been designated, the Head Delegate will just have to wait for the Form B (available from October 3rd).</p>
        <br/>
        <p>ILC and IPC only:<br/>
            Delegates who want to participate in the International Law Commission and the International Press Committee have to fill a specific form for registering. Positions will be assigned on a first-come, first-served basis.</p>
