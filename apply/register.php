                <h5>How to register?</h5>
                <br/>
                <h6>Registering for UFRGSMUN 2014 is really easy. You just have to follow a few steps.</h6>
                <p>
                    <strong>Step 1: Putting together a delegation and selecting a head delegate</strong>
                </p>
                <p>
                    The first thing to do when putting together a delegation would be to find other
                    people that wish to take part in the other committees. Next, you should select
                    one or more countries from the <a href="?page=conference&section=countries">Country/Committee
                    Matrix</a> that have representatives in all the committees that you and your friends want
                    to participate in. Delegations may range from 1 (one) up to 20 (twenty) delegates. Then,
                    you should select a head delegate among the members from your delegation. That person will
                    be responsible for filling out registration forms, making payments, and staying in direct
                    contact with UFRGSMUN staff.

                </p>
                <p>
                    If you wish to represent a specific country, then you should invite enough people to
                    complete the delegation of this country, settling amongst yourselves who will be in
                    which committee. But remember that it is not certain that your first choice of country
                    will be designated to you. The number of delegates and the countries that you wish to
                    represent must be informed in Form A, which will be online from August 17th until
                    September 30th.
                </p>
                <p class="references">
                    NOTE: if you do not take part in a delegation and wish to be fitted in an existing
                    one, specific guidance will be provided to you in Form A.
                </p>
            </section>
        </div>
