                    Once finished, the transfer receipt must be sent to our email or the registration
                    will not be completed. After that, head delegates will receive a confirmation e-mail
                    from UFRGSMUN staff. We strongly encourage head delegates to make just one transfer
                    for the whole delegation so as to make the registration process easier. When the full
                    amount or the first installment of the payment and the transfer receipt are received
                    by the Staff, the Head Delegate will receive a confirmation e-mail. Another confirmation
                    e-mail will be sent once the second installment is paid.
                </p>
