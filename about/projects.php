				<h3 class="center">UFRGSMUNDI</h3>
				<p>
					Awarded at UFRGS Extension Hall in 2014, UFRGSMUNDI aims to promote the discussion of important topics on the international agenda among high school students from public and private schools of Rio Grande do Sul. Discussions are structured in committees that simulate international diplomatic conferences and courts of justice, in which the participants act as representatives of countries, judges and reporters. Thus, the project is a tool for those interested in teaching and learning about International Relations, thereby contributing to personal and intellectual growth of the students. It also provides them opportunities to know more about other realities and helps with the developomment of their communication, argumentation, negotiation and leadership skills.
					Organized by undergraduates from various courses – International Relations, Law, Communication and Design – UFRGSMUNDI provides participants with supporting materials made and reviewed by undergraduate and postgraduate students from UFRGS. These are presented in a Study Guide format, which aim to supply delegates with an excellent and easy to understand material, this way assisting their preparation for simulation. The guides are compiled in an annual academic journal, which is printed and distributed to participating schools before the event.
					This way, by combining participants from the university and secondary schools, the project also promotes the exchange of experiences between the University and the Society, bringing together realities that often seem distant.
				</p>
