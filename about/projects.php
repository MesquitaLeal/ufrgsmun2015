				<h3 class="center">RIPE</h3>
				<p>
					The project aims to promote the democratization of the debate of International Relations in schools, taking as a starting point a comparative approach between the brazilian and international political reality. Therefore, RIPE tries to encourage interest and the construction of a critical view among brazilian students, providing subsidies to the continuing education of teachers in the study and understanding of national and international political phenomena.
					The RIPE consists of a short course on topics of International Relations for history, geography, and sociology teachers from public schools in the metropolitan region of Porto Alegre, aiming to encourage the discussion of International Relations issues in the classroom. It occurs during the first semester of each year and in 2016 will have its sixth edition.
				</p>
