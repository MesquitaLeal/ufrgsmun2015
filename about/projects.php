				<h3 class="center">UFRGSMUN Back in School</h3>
				<p>
					The UFRGSMUN BIS (Back in School) project, created in 2008, aims to take the relevant debates in the current international scenario to the classrooms of the public high schools of Porto Alegre and its metropolitan region. To this end, BIS, managed from the beginning by students from UFRGS, uses the model of the United Nations conference simulations as a teaching tool. This model is already established in the academia, as a graduation activity in various universities around the world.
					The main objective of BIS is to bring the topics of International Relations to public school students in order to sharpen critical thinking, practice oratory, and stimulate debate about global issues. UN models are events where students simulate UN agencies and comitees, acting as delegates, representing and defending the foreign policies of countries. BIS, however, is not restricted to UN agencies, extending its scope to regional organizations such as CELAC and the African Union. Today, the BIS team consists of college students of various courses.
				</p>
