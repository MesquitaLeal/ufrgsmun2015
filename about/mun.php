            <h2>Model United Nations (MUN)</h2>
        </div>
    </div>
    <div class="row purple-50 section-p content">
        <div class="col-sm-6">
            <p>
                MUN are opportunities for students to debate relevant
                issues of the international agenda through an authentic simulation of the
                discussions taking place at the UN. They simulate, in a very realistic manner,
                the proceedings and debates of different UN organs, such as the Security
                Council, the General Assembly, and the Economic and Social Council. During the
                simulations, the students act as "delegates" representing one specific country.
                By acting as ambassadors of that Member State, participants support their
                positions and uphold their interests during the discussions. The simulation of
                international organizations begun in the first half of the 20th century, with
                simulations of the League of Nations, and has grown progressively more popular
                through the years.
            </p>
        </div>
        <div class="col-sm-6">
            <p>
                The easier information flows worldwide, the more people
                become conscious of the importance of knowing and discussing collective problems
                of the world. That is probably why, today, more than 200,000 high school and
                college/university students take part in this challenging experience every year.
                To date, more than 1,000,000 people have participated in Model UN around the
                world. Many of them have become great leaders in politics, business, education,
                law, and even in arts, medicine and engineering. The goal of the Model UN is to
                engage students in the work of the UN and other multilateral organizations,
                through research, discussion, negotiation, and debate. It has proven to be a
                rewarding educational activity, providing participants with a better
                understanding of the function, scope and issues of bodies of different
                international organizations.
            </p>
        </div>
    </div>
