            <h2>International Press Committee</h2>
        </div>
    </div>
    <div class="row orange-50 section-p content">
        <div class="col-lg-12">
            <p>
                Not so rarely, the importance of media in international relations decisions are underrated. It is known that vehicles follow their editorial line and do not broadcast the news in an impartial manner in most events. The journalists that report the stories are subjected to writing according to their beliefs, political views, agency policies and country position - which is why there are many different impressions about one situation. This variety of views - from both the journalist and the vehicle he writes for - explains the many different responses about a fact.
            </p>
            <p>
                The way the media behaves and the positions it has is also affected by its relation with the other committees - not only the fact itself but also the decision of a country to provide information or not influences the way the news are delivered to the public (not to mention how the estate in question is pictured), intentionally or not.  It would be unrealistic to think that the publications, the opinion and the behavior of journalists in such events would not affect the course of the UN discussions.
            </p>
            <p>
                In 2015, UFRGSMUN invites aspiring journalists again to join our International Press Committee. The IPC, as we call it, will be responsible for collecting and sharing information about what is being discussed in the committees, remaining faithful to the perceptions of international media, from the gigantic westerns like BBC and The New York Times, to the local media of all five continents. This experience challenges not only journalists to deal with UN proceedings, but intends to show participants how important the media is to international affairs, bringing students to participate in a real media-government relationship: its problems, clashes maneuvers and partnerships.
            </p>
